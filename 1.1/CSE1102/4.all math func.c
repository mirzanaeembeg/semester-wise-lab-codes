#include <math.h>
#include <stdio.h>
int main()
{
    int a,b=2.7,c=90,d=45;
    a=pow(6,4);
    printf("Square root of 99 = %.3lf",sqrt(99));
    printf("\n6^4 = %d",a);
    printf("\nLog10(2.7)=%.3lf",log(b));
    printf("\nSine of 90 degrees=%.3lf",sin(c));
    printf("\nCosine of 45 degrees=%.3lf",cos(d));
    printf("\nTangent of 45 degrees=%.3lf",tan(d));

    return 0;
}
